    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed

        /*
        LA TABLA inventario DE LA BD SE COMPONE DE 4 CAMPOS:
          IdProducto: INT
          Nombre: STRING
          CantidadKg: INT
          PrecioKg: DOUBLE
        
        LO QUE HAREMOS AHORA ES OBTENER VALORES PARA ESOS CAMPOS Y DE ESA
        MANERA PODER AGREGAR NUEVOS PRODUCTOS A LA TABLA inventario
        */
        
        /*
        obtenemos el texto que se ingresó en el campo txtCodigo, lo convertimos
        en un int por medio de un "Integer.parseInt y luego lo asignamos a una 
        variable idp de tipo int
        */
        int idp = Integer.parseInt(txtCodigo.getText());
        
        
        /*
        obtenemos el texto que se ingresó en el campo txtNombre, y lo asignamos
        a una variable de tipo String
        */
        String nombre = txtNombre.getText();
        
        
        /*
        obtenemos el texto que se ingresó en el campo txtCantidad, lo convertimos
        en un int por medio de un "Integer.parseInt y luego lo asignamos a una
        variable cantidad de tipo int
        */
        int cantidad = Integer.parseInt(txtCantidad.getText());
        
        
        /*
        obtenemos el texto que se ingresó en el campo txtPrecio, lo convertimos
        en un double por medio de un "Double.parseDouble y luego lo asignamos a una
        variable precio de tipo double
        */
        double precio = Double.parseDouble(txtPrecio.getText());
        
        Inventario nuevoInventario = new Inventario(idp, nombre, cantidad, precio);
        
        
        /*
        Instrucción que se quiere realizar junto con los nombres de las
        columnas y sus parametros correspondientes
        */
        
        ConectaBD cnc = new ConectaBD();
        Connection cnx = cnc.conectar();
        
        String sql = " INSERT INTO inventario (IDProducto, Nombre, CantidadKg, PrecioKg) VALUES ( "+nuevoInventario.getIdProducto()+" , '"+nuevoInventario.getNombre()+"' , "+nuevoInventario.getCantidad()+" , "+nuevoInventario.getPrecioCompra()+" ) ";
        
        try {
            // para que se pueda comunicar la instrucción a la bd
            Statement instruccion = cnx.createStatement(); //
            // ejecutando la instrucción y actualizando la bd
            instruccion.executeUpdate(sql);           
        } catch (Exception e) {
            Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "ERROR");
        }
        JOptionPane.showMessageDialog(null, "Producto agregado satisfactoriamente");
    }//GEN-LAST:event_btAgregarActionPerformed

